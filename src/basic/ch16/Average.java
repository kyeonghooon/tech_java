package basic.ch16;

public class Average {

	public double average(double a, double b) {
		return (a + b)/2.0;
	}
	public double average(double a, double b, double c) {
		return (a + b + c)/3.0;
	}
	public int average(int a, int b) {
		return (a + b)/2;
	}
	public int average(int a, int b, int c) {
		return (a + b + c)/3;
	}
}
